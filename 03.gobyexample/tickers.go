package main

import (
	"fmt"
	"time"
)

func main() {
	ticker := time.NewTicker(500 * time.Millisecond)
	done := make(chan bool)

	go func() {
		for {
			select {
			case <-done : // receive (<- chan)
				return
			case t := <-ticker.C : // receive (<- Ticker)
				fmt.Println("Tick at", t)
			}
		}
	}()

	time.Sleep(1600 * time.Millisecond)
	ticker.Stop()
	done <- true // sends (chan <-)
	fmt.Println("Ticker stopped")
}